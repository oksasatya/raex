    Kura-Kura adalah hewan bersisik berkaki empat yang
    termasuk golongan
    reptil. Bangsa hewan
    yang
    disebut (ordo) Testudines (atau Chelonians) ini khas dan mudah dikenali dengan adanya ‘rumah’
    atau batok (bony shell) yang keras dan kaku.

    Batok kura-kura ini terdiri dari dua bagian. Bagian atas yang menutupi punggung disebut karapas
    (carapace) dan bagian bawah (ventral, perut) disebut plastron. Kemudian setiap bagiannya ini
    terdiri dari dua lapis. Lapis luar umumnya berupa sisik-sisik besar dan keras, dan tersusun
    seperti genting; sementara lapis bagian dalam berupa lempeng-lempeng tulang yang tersusun rapat
    seperti tempurung. Perkecualian terdapat pada kelompok labi-labi (Trionychoidea) dan jenis penyu
    belimbing, yang lapis luarnya tiada bersisik dan digantikan lapisan kulit di bagian luar
    tempurung tulangnya.

    Dalam bahasa Indonesia, kita mengenal tiga kelompok hewan yang termasuk bangsa ini, ialah penyu
    (bahasa Inggris: sea turtles), labi-labi atau bulus (freshwater turtles), dan kura-kura
    (tortoises). Dalam bahasa Inggris, dibedakan lagi antara kura-kura darat (land tortoises) dan
    kura-kura air tawar (freshwater tortoises atau terrapins).
